function fizzBuzz() {
    
    for(var i = 1; i <= 15; i++){

        //detect values divisble by 3 and 5
        if(i % 3 === 0 && i % 5 === 0){
            //push to the array
            console.log("fizzbuzz");
        }
        //detect values divisible by 3
        else if(i % 3 === 0){
            console.log("fizz");
        }
        //is it values divisible by 5
        else if(i % 5 === 0){
            console.log("buzz");
        }
        else {
          console.log(i);
        }
        
    }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(number) {
 let size = number;

let board = "";

for (let y = 0; y < size; y++) {
  for (let x = 0; x < size; x++) {
    if ((x + y) % 2 == 0) {
      board += " ";
    } else {
      board += "#";
    }
  }
  board += "\n";
}

console.log(board);
}

